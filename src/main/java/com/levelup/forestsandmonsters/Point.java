package com.levelup.forestsandmonsters;


public class Point {
   int xCoordinate;
   int yCoordinate;


public Point(int xCoordinate,int yCoordinate)
{

this.xCoordinate = xCoordinate;
this.yCoordinate = yCoordinate;
}


public Object getXCoordinate() {
    return xCoordinate;
}


public Object getYCoordinate() {
    return yCoordinate;
}

}
